################################################################################
# Basic if else statement
################################################################################

def basic_if(x):
    if x < 5:
        y = 'x < 5'
    else:
        y = 'x >=5'
    return y

################################################################################
# Basic nested if else statement
################################################################################

def basic_nested_if(x):
    if x < 5:
        y = 'x < 5'
    else:
        if x < 10:
            y = '5 <= x < 10'
        else:
            y = 'x >= 10'
    return y

################################################################################
# Introducing elif
################################################################################

def simple_ifelse(x):
    if x < 5:
        y = 'x < 5'
    elif x < 10:
        y = '5 <= x < 10'
    elif x < 15:
        y = '10 <= x < 15'
    elif x < 20:
        y = '15 <= x < 20'
    else:
        y = 'x >= 20'
    return y

################################################################################
# X returned if (condition is true) else return Y
################################################################################

def var_condition(x):
    y = 'x < 5' if x < 5 else 'x >=5'
    return y 
